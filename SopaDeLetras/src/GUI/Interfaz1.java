    public void MostrarTablero(){
        char[][]tableroLetras=procesador.getTableroLetras();
        for (int i = 0; i < tablero.length; i++) {
            for (int j = 0; j < tablero[i].length; j++) {
                tablero[i][j].setText(Character.toString(tableroLetras[i][j]));
            }
        }
    }
    
